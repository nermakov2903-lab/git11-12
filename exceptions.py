"""
Модуль exceptions содержит кастомные классы исключений для проекта.

Каждое исключение предназначено для обработки конкретной ошибки в программе,
что позволяет разделять логику выполнения алгоритмов и обработку ошибок.

Использование кастомных исключений:
- Позволяет ловить специфические ошибки.
- Облегчает ведение логов.
- Упрощает тестирование и локализацию сообщений об ошибках.
"""

class TaskError(Exception):
    """
    Базовое исключение для всех ошибок задания.

    Наследуется всеми специфическими исключениями:
    EmptyArrayError, InvalidMenuChoiceError, InvalidNumberError.

    Применение:
        Можно ловить все ошибки задания через `except TaskError`.
    """
    pass


class EmptyArrayError(TaskError):
    """
    Исключение возникает, если массивы не были введены пользователем
    до выполнения анализа.

    Атрибуты:
        message (str): Сообщение об ошибке.

    Пример использования:
        if arr1 is None or arr2 is None:
            raise EmptyArrayError("Массивы должны быть введены.")
    """
    pass


class InvalidMenuChoiceError(TaskError):
    """
    Исключение возникает, если пользователь выбрал пункт меню, которого нет.

    Атрибуты:
        choice (str): Пункт меню, который вызвал ошибку.

    Пример использования:
        if choice not in valid_choices:
            raise InvalidMenuChoiceError(choice)
    """
    pass


class InvalidNumberError(TaskError):
    """
    Исключение возникает, если пользователь ввел не число там, где ожидалось целое число.

    Атрибуты:
        value (any): Некорректное значение, вызвавшее ошибку.

    Пример использования:
        try:
            n = int(input("Введите число: "))
        except ValueError:
            raise InvalidNumberError("Введено не число")
    """
    pass
