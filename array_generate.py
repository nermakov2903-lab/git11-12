"""
Модуль для генерации массивов случайных чисел.
Используется в случаях, когда пользователь выбирает автоматический ввод.
"""

import random

def generate_array(n=10, a=0, b=100):
    """
    Генерирует массив случайных целых чисел.

    Args:
        n (int): Количество элементов в массиве. По умолчанию 10.
        a (int): Нижняя граница диапазона значений. По умолчанию 0.
        b (int): Верхняя граница диапазона значений. По умолчанию 100.

    Returns:
        list[int]: Список случайных чисел длины n.
    """
    return [random.randint(a, b) for _ in range(n)]
