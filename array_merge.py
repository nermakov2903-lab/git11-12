"""
Модуль для объединения массивов.
Используется как вспомогательная функция в ранних итерациях разработки.
"""

def merge_arrays(a, b):
    """
    Объединяет два массива в один.

    Args:
        a (list): Первый список.
        b (list): Второй список.

    Returns:
        list: Новый список, содержащий элементы из обоих массивов.
    """
    return a + b
