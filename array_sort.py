"""
Модуль для сортировки массива чисел.
Используется как пример функции первой итерации проектирования.
"""

def sort_array(arr):
    """
    Возвращает отсортированную версию массива.

    Args:
        arr (list[int]): Исходный массив чисел.

    Returns:
        list[int]: Отсортированный массив.
    """
    return sorted(arr)
