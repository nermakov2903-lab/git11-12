"""
Модуль для ручного ввода массивов чисел пользователем.
Используется в заданиях, где требуется ввод исходных данных.
"""

def manual_input_array():
    """
    Выполняет ввод массива чисел с клавиатуры.

    Ожидается строка вида:
        1 2 3 4 5

    В процессе ввода выполняется:
        — Считывание строки
        — Разделение по пробелам
        — Преобразование каждого элемента в целое число

    Returns:
        list[int]: Список введённых пользователем чисел.
    """
    print("Введите числа через пробел:")
    return list(map(int, input().split()))
